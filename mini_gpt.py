"""Stack Multiple Blocks - GPT"""

class MiniGPT(nn.Module): # Corrected nn.module to nn.Module
  def __init__(self, vocab_size, d_model, num_heads, d_ff, num_layers, max_seq_len):
      super().__init__()

      self.token_emb = nn.Embedding(vocab_size, d_model)
      self.pos_emb = nn.Embedding(max_seq_len, d_model)

      self.blocks = nn.ModuleList([
          TrasnformerBlock(d_model, num_heads, d_ff)
          for _ in range(num_layers)
      ])

      self.head = nn.Linear(d_model, vocab_size)

  def forward(self, token_ids):
      batch, seq_len = token_ids.shape

      positions = torch.arange(seq_len).unsqueeze(0)
      x = self.token_emb(token_ids) + self.pos_emb(positions)

      for block in self.blocks:
          x = block(x)

      logits = self.head(x)
      return logits


