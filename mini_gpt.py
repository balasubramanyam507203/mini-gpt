"""Generation with stop token"""

def generate_with_stop(model, input_ids, max_new_tokens=20,
                        temperature=1.0, eos_token_id=0):
    """
    eos_token_id → end of sequence token
                   generation stops when this token is produced
    """

    model.eval()
    current_ids = input_ids.clone()

    with torch.no_grad():
        for step in range(max_new_tokens):

            # Forward pass
            logits = model(current_ids)
            next_logits = logits[:, -1, :]

            # Temperature + sample
            probs = F.softmax(next_logits / temperature, dim=-1)
            next_token = torch.multinomial(probs, num_samples=1)

            # Append
            current_ids = torch.cat([current_ids, next_token], dim=1)

            print(f"Step {step+1}: token = {next_token.item()}")

            # Stop if end token generated
            if next_token.item() == eos_token_id:
                print("→ EOS token reached. Stopping.")
                break

    return current_ids


